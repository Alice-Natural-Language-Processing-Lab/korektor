Korektor Spellchecker
=====================

Rozšíření Korektor Spellchecker umožňuje použití služby Korektor
(nástroje pro kontrolu pravopisu a někdy také gramatiky vyvinutého
Ústavem formální a aplikované lingvistiky na Karlově Universitě
v Praze) na libovolných editačních prvcích webových stránek.

Pomocí tohoto rozšíření je možné buď přímo provést opravu obsahu
editačního prvku, nebo zobrazit dialog s možnými opravami. Tento
dialog se zobrazuje vložením přímo do původní stránky, takže nemusí
fungovat spolehlivě na stránkách, které nebyly testovány. Nicméně
na mnoha stránkách funguje rozšíření bez problémů.

Ačkoliv jsou zdrojové kódy služby Korektor i tohoto rozšíření
k dispozici pod licencí BSD-3-Clause, prosím respektujte licenci
CC BY-SA-NC použitých jazykových modelů.

Služba Korektor je popsána na https://lindat.mff.cuni.cz/services/korektor/ ,
Korektor samotný je k dispozici na http://ufal.mff.cuni.cz/korektor .
